let products = {
  paintings: [],
  photos: [],
  sculptures: [],
  digital: []
}

const populateExec = () => {
  categories.forEach(cat => {
    if (cat === "paintings") {
      paintings.forEach(img => populate(cat, img))
    } else if (cat === "sculptures") {
      sculptures.forEach(img => populate(cat, img))
    } else if (cat === "digital") {
      digital.forEach(img => populate(cat, img))
    } else if (cat === "photos") {
      photos.forEach(img => populate(cat, img))
    }
  })
}

const populate = (cat, img) => {
  products[cat].push({
    id: uuidv4(),
    title: uniqueNamesGenerator({
      dictionaries: [colors, adjectives],
      length: Math.floor(Math.random() * 2),
      style: 'capital',
      separator: ' '
    }),
    description: loremIpsum({
      count: 15,
      format: "plain",
      units: "words",
    }),
    price: prices[Math.floor(Math.random() * prices.length)],
    category: cat,
    image: img,
    artist: uniqueNamesGenerator({
      dictionaries: [names, names],
      length: Math.floor(Math.random() * 2),
      style: 'capital',
      separator: ' '
    })
  })
}
populateExec()


export default products;
